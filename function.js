// Function to compute the product of p1 and p2
function perkalian(p1, p2) {
  return p1 * p2;
}

let result = perkalian(9, 3);
let result2 = perkalian(8, 8);
console.log(result);
console.log(result2);

function coba(angka1, angka2, angka3) {
  return (angka1 * angka2) / angka3;
}

let result3 = coba(10, 8, 4);
console.log(result3);
